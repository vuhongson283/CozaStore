    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    SettingDAO settingDAO = new SettingDAO();
    SettingCategoryDAO settingCategoryDAO = new SettingCategoryDAO();

    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        SignUpInDAO d = new SignUpInDAO();
        HttpSession session = request.getSession();
        Users u = (Users) session.getAttribute("acc");
        String emailu = u.getUser_email();
        Users user = d.GetUser(emailu);
        request.setAttribute("profileuser", user);
        try {
            ArrayList<SettingCategory> settingCategorys = settingCategoryDAO.getAllSettingCategory();
            request.setAttribute("settingCategorys", settingCategorys);
            request.getRequestDispatcher("AddNewSetting.jsp").forward(request, response);
        } catch (SQLException ex) {
            Logger.getLogger(AddNewSetting.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        SignUpInDAO d = new SignUpInDAO();
        HttpSession session = request.getSession();
        Users u = (Users) session.getAttribute("acc");
        String emailu = u.getUser_email();
        Users user = d.GetUser(emailu);
        request.setAttribute("profileuser", user);
        String settingCategory = request.getParameter("type");
        String settingValue = request.getParameter("value");
        String settingStatus = request.getParameter("status");
        UploadImage uploadImage = new UploadImage();
        String avatar = "";
        if (request.getPart("photo").getSize() > 0) {
            avatar = (String) uploadImage.uploadFile(request, "images");
        }
        settingDAO.addNewSetting(settingCategory, settingValue, settingStatus, avatar);
        response.sendRedirect("settingmanagement");
    }

